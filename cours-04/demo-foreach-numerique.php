<?php

// Création de mon tableau (array) numérique $etudiants
$etudiants = [
    'Étienne',
    'Anh-tuân',
    'Daniel',
    'Pierre',
];

// Pour parcourir le tableau
// foreach pour tableaux numériques
// La première variable dans le foreach doit correspondre exactement au nom
// de la variable
// Le nom de la deuxième variable est libre à vous
foreach($etudiants as $patate) {
    echo $patate . '<br>';
}

?>